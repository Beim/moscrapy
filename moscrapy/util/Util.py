def get_random_proxy(type='HTTP'):
    # url = 'http://119.29.160.85:8899/api/v1/proxies?limit=10'
    if type == 'HTTPS':
        randmax = 60
    else:
        randmax = 5
    url = 'http://119.29.160.85:8899/api/v1/proxies?limit=10&anonymous=true&page=%d' % random.randint(2, randmax)
    if type == 'HTTPS': url = url + '&https=true'
    res = requests.get(url)
    jsondata = json.loads(res.content.decode('utf-8'))
    rand_proxy = random.choice(jsondata['proxies'])

    # res = dbCursorProxy.query('select host, port from proxy where hit>1 and miss=0 and type="%s" order by timestamp desc limit 10' % type)
    # proxy_info = res[randint(0, len(res) - 1)]
    return '%s://%s:%s' % (type.lower(), rand_proxy['ip'], rand_proxy['port'])

